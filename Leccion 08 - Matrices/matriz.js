// Matrices en JS
// Declaration de una matriz
let matriz = [[], []];

//Modificar los valores
//Renglo o fila 0
matriz[0][0] = 100;
matriz[0][1] = 200;
matriz[0][2] = 300;

//Renglon o fila 1
matriz[1][0] = 400;
matriz[1][1] = 500;
matriz[1][2] = 600;

//Leer valores
console.log(`Elemento [0][1] = ${matriz[0][1]}`);
console.log(`Elemento [1][2] = ${matriz[1][2]}`);
