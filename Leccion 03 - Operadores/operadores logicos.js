// Operadores Logicos
let a = true;
let b = false;
console.log(a);
console.log(b);

//Operadores Logicos && (and o Y)
console.log(`${a} && ${b} -> ${a&&b}`);


//Operador Logico || (or o O)
console.log(`${a} || ${b} -> ${a||b}`);

//Operador ! (not o NO)
console.log(`${a} -> ${!a}`);
console.log(`${b} -> ${!b}`);



